<h1>Minecraft Crowd Control</h1>

<p>
    Minecraft <a href="https://crowdcontrol.live/">Crowd Control</a> is a Minecraft mod that allows your livestream
    viewers on any platform to interact with your game using tips, bits, channel points, charitable donations, and more.
    It can run as a client mod or as a server plugin.
</p>

